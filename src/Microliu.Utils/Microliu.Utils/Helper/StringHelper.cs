        }


        /// <summary>
        /// 获取邮箱域名，不含@
        /// </summary>
        /// <param name="email">邮箱地址</param>
        /// <returns></returns>
        public static string GetEmailDomain(string email)
        {
            email = email ?? "";
            if (!email.Contains("@")) return "";
            return email.Substring(email.LastIndexOf('@')).Replace("@","");
        }
